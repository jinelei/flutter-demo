class Debug {
  static log(String tag, String text) {
    print('[$tag] $text');
  }

  static info(String tag, String text) {
    print('[$tag] $text');
  }

  static success(String tag, String text) {
    print('[$tag] $text');
  }

  static error(String tag, String text) {
    print('[$tag] $text');
  }
}
